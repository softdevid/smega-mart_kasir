    private void jButtonEditPelangganEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditPelangganEditActionPerformed
        // TODO add your handling code here:
        if(jTextFieldKdPelangganEdit.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Masukan Kode Pelanggan");
        }else if(jTextFieldNmPelangganEdit.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Masukan Nama Pelanggan");
        }else if(jTextFieldPointEdit.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Masukan Jumlah Point");
        }
        else{
            String KdPlnggn = jTextFieldKdPelangganEdit.getText();
            String namaPlnggn = jTextFieldNmPelangganEdit.getText();
            String point = jTextFieldPointEdit.getText();
            
            String query ="update `datapelanggan` set `kdPelanggan`= '"+KdPlnggn+"', `namaPelanggan`='"+namaPlnggn+"', `Point` ='"+point+"' where kdPelanggan = '"+KdPlnggn+"'";
            try {
                int pilihan = JOptionPane.showConfirmDialog(null, "Simpan data?","Peringatan",JOptionPane.YES_NO_OPTION);
                if(pilihan==JOptionPane.YES_OPTION){
                    PreparedStatement ps = (PreparedStatement) conn.prepareStatement(query);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                    jDialogEdit.setVisible(false);
                    jDialogEdit.dispose();
                    clear();
                    load_table();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        
        }
    }//GEN-LAST:event_jButtonEditPelangganEditActionPerformed

    private void jTablePelangganMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePelangganMouseClicked
        // TODO add your handling code here:
        // menampilkan data kedalam form pengisian:
        
        int baris = jTablePelanggan.rowAtPoint(evt.getPoint());
        String kdPelanggan =jTablePelanggan.getValueAt(baris,1 ).toString();
        jTextFieldKdPelangganEdit.setText(kdPelanggan);
        
        String query = "SELECT kdPelanggan, namaPelanggan, Point from dataPelanggan where kdPelanggan = '"+kdPelanggan+"'";
        try {
            Statement stm=conn.createStatement();
            ResultSet res=stm.executeQuery(query);
            
            if(res.next()){
                jTextFieldNmPelangganEdit.setText(res.getString("namaPelanggan"));
                jTextFieldKdPelangganEdit.setText(res.getString("kdPelanggan"));
                jTextFieldPointEdit.setText(res.getString("Point"));
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jTablePelangganMouseClicked

