    private void jButtonSimpanUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanUserActionPerformed
        if(jTextFieldkdUser.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Masukan Kode User");
        }else if(jTextFieldnamaUser.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Masukan Nama User");
        }else if(jPasswordFielduser.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Masukan Password");
        }
        else{
            String kduser = jTextFieldkdUser.getText();
            String namauser = jTextFieldnamaUser.getText();
            char[] pwduser = jPasswordFielduserEdit.getPassword();
            String leveluser = jComboBox1.getSelectedItem().toString();
            
            String query ="INSERT INTO `datauser` (`kdUSer`, `namaUser`, `password`, `level`) VALUES ('"+kduser+"', '"+namauser+"', '"+pwduser+"', '"+leveluser+"')";
            try {
                int pilihan = JOptionPane.showConfirmDialog(null, "Simpan data?","Peringatan",JOptionPane.YES_NO_OPTION);
                if(pilihan==JOptionPane.YES_OPTION){
                    PreparedStatement ps = (PreparedStatement) conn.prepareStatement(query);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                    clear();
                    load_table();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
        clearInput();
    }

