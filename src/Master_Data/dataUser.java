    private void jTableuserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableuserMouseClicked
        // TODO add your handling code here:
        // menampilkan data kedalam form pengisian:
        
        int baris = jTableuser.rowAtPoint(evt.getPoint());
        String kduser =jTableuser.getValueAt(baris, 1).toString();
        jTextFieldKodeUserEdit.setText(kduser);
        
        String query = "SELECT kdUser, namaUser, password, level from datauser where kdUser = '"+kduser+"'";
        try {
            Statement stm=conn.createStatement();
            ResultSet res=stm.executeQuery(query);
            
            if(res.next()){
                jTextFieldkdUser.setText(res.getString("kdUser"));
                jTextFieldnamaUser.setText(res.getString("namaUser"));
                jPasswordFielduser.setText(res.getString("password"));
                jTextFieldnamaUser.setText(res.getString("level"));
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jTableuserMouseClicked

