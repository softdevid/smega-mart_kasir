    private void jTableSupplierMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSupplierMouseClicked
        // TODO add your handling code here:
        // menampilkan data kedalam form pengisian:
        
        int baris = jTableSupplier.rowAtPoint(evt.getPoint());
        String kdSpplier =jTableSupplier.getValueAt(baris, 0).toString();
        jTextFieldKodeSupplierEdit.setText(kdSpplier);
//        String nmSpplier =jTableSupplier.getValueAt(baris, 1).toString();
//        jTextFieldNamaSupplierEdit.setText(nmSpplier);
        
        String query = "SELECT kdSupplier, namaSupplier from datasupplier where kdSupplier = '"+kdSpplier+"'";
        try {
            Statement stm=conn.createStatement();
            ResultSet res=stm.executeQuery(query);
            
            if(res.next()){
                jTextFieldKodeSupplierEdit.setText(res.getString("kdSupplier"));
                jTextFieldNamaSupplierEdit.setText(res.getString("namaSupplier"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jTableSupplierMouseClicked

