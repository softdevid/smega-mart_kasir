    private void jButtonSimpanSupplierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanSupplierActionPerformed
        if(jTextFieldKdSupplierOK.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Masukan Kode Supplier");
        }else if(jTextFieldNamaSuplierOk.getText().length()==0){
            JOptionPane.showMessageDialog(null, "Masukan Nama Supplier");
        }else{
            String kdsupplier = jTextFieldKdSupplierOK.getText();
            String namasupplier = jTextFieldNamaSuplierOk.getText();
           
            
            String query ="INSERT INTO `datasupplier` (`kdSupplier`, `namasupplier`) VALUES ('"+kdsupplier+"', '"+namasupplier+"')";
            try {
                int pilihan = JOptionPane.showConfirmDialog(null, "Simpan data?","Peringatan",JOptionPane.YES_NO_OPTION);
                if(pilihan==JOptionPane.YES_OPTION){
                    PreparedStatement ps = (PreparedStatement) conn.prepareStatement(query);
                    ps.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
                    clear();
                    load_table();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }

