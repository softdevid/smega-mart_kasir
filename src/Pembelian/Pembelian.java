    private void txtBarcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBarcodeKeyPressed
        if (evt.getKeyCode()==KeyEvent.VK_ENTER) {
            String barcode = txtBarcode.getText();
            String query = "select namaBarang,hrgJual,hrgBeli from databarang where barcode ='"+barcode+"'";
            
            try {
                Statement stm=conn.createStatement();
                ResultSet rs=stm.executeQuery(query);

                if(rs.next()){
                    txtnmBarang.setText(rs.getString("namaBarang"));

                    double hrgJual = Double.parseDouble(rs.getString("hrgJual"));
                    jTextFieldhrgJual.setText(String.format("%,.2f", hrgJual));

                    double hrgBeli = Double.parseDouble(rs.getString("hrgBeli"));
                    jFormattedTextFieldhrgBeli.setText(String.format("%,.2f", hrgBeli));

                    jFormattedTextFieldhrgBeli.requestFocus();
                } else {
                    txtnmBarang.setText("");
                    jTextFieldhrgJual.setText("0");

                    txtBarcode.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_txtBarcodeKeyPressed

