    private void load_tabelBeli() {
     String nomorFakturBeli = jTextFieldnoFakturPembelian.getText();
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Barcode");
        model.addColumn("Nama Barang");
        model.addColumn("Jumlah Beli");
        model.addColumn("Harga Beli");
        model.addColumn("Sub Total");
        
        //menampilkan data database kedalam tabel
        try {
            int no=1;
            String sql = "SELECT tabelrealpembelian.barcode, databarang.namaBarang, tabelrealpembelian.jmlBeli, tabelrealpembelian.hrgBeli, tabelrealpembelian.jmlBeli * tabelrealpembelian.hrgBeli AS subtotal from tabelrealpembelian,databarang WHERE tabelrealpembelian.barcode = databarang.barcode AND tabelrealpembelian.noFakturBeli='"+nomorFakturBeli+"'";
            Statement stm=conn.createStatement();
            ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                Object[]o=new Object[6];
                o[0]= no++;
                o[1]= res.getString("barcode");
                o[2]= res.getString("namaBarang");
                
                double j = Double.parseDouble(res.getString("jmlBeli"));
                o[3]= String.format("%,.2f", j);
                
                double h = Double.parseDouble(res.getString("hrgBeli"));
                o[4]= String.format("%,.2f", h);
                
                double i = Double.parseDouble(res.getString("Subtotal"));
                o[5]= String.format("%,.2f", i);
                
                model.addRow(o);
            }
            tabelPembelian.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }   
    }

    private void tampil_total() {
        String nomorFaktur = jTextFieldnoFakturPembelian.getText();
        String query="select ifnull(sum(hrgBeli*jmlBeli),0) as Total from tabelrealpembelian where noFakturBeli = '"+nomorFaktur+"'";
        try {
            Statement stm=conn.createStatement();
            ResultSet res=stm.executeQuery(query);
            
            if(res.next()){
                double h = Double.parseDouble(res.getString("Total"));
                jLabeltotalBeli.setText(String.format("%,.2f", h));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

